from OpenGL.GL import *
from OpenGL.GLU import *
from OpenGL.GLUT import *
import sys
def init():
    glClearColor(0.0, 0.0, 0.0, 0.0)
    gluOrtho2D(-100.0, 100.0, -100.0, 100.0)
def randompoints():
    glClear(GL_COLOR_BUFFER_BIT)
    glColor3f(1.0, 0.0, 0.0)
    glBegin(GL_LINES)
    glVertex2f(60,-30)
    glVertex2f(60,30)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(60,-30)
    glVertex2f(60,30)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(60,30)
    glVertex2f(30,60)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(30,60)
    glVertex2f(-30,60)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(-30,60)
    glVertex2f(-60,30)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(-60,30)
    glVertex2f(-60,-30)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(-60,-30)
    glVertex2f(-30,-60)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(-30,-60)
    glVertex2f(30,-60)
    glEnd()
    glBegin(GL_LINES)
    glVertex2f(30,-60)
    glVertex2f(60,-30)
    glEnd()
    glFlush()
def main():
    glutInit(sys.argv)
    glutInitDisplayMode(GLUT_SINGLE|GLUT_RGB)
    glutInitWindowSize(500,500)
    glutInitWindowPosition(50,50)
    glutCreateWindow("Random Points")
    glutDisplayFunc(randompoints)
    init()
    glutMainLoop()
main()
